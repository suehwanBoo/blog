import { useEditorActiveStyle } from "../hooks/useEditorActiveStyle";
import useTiptap from "../hooks/useTiptap";
import { buttonStyles as styles } from "./Button.css";

const alignMaps = {
  left: {
    icon: LeftAlign,
  },
  right: {
    icon: RightAlign,
  },
  center: {
    icon: CenterAlign,
  },
} as const;

type AlignState = keyof typeof alignMaps;

export default function TextAlign({ align }: { align: AlignState }) {
  const editor = useTiptap();
  const isHeadingActive = useEditorActiveStyle("heading", { textAlign: align });
  const isParagraphActive = useEditorActiveStyle("paragraph", {
    textAlign: align,
  });
  const toggleAlign = () => {
    editor?.chain().focus().toggleTextAlign(align).run();
  };
  const icon = alignMaps[align].icon;
  return (
    <button
      className={styles.button({
        active: isHeadingActive || isParagraphActive,
      })}
      onClick={toggleAlign}
      aria-label={`${align} align`}
    >
      {icon()}
    </button>
  );
}

